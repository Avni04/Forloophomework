package homeworkjan;

public class ForLoopEvenOdd {

    public static void main(String[] args) {
        System.out.println("Odd Numbers");
        for (int o=1; o<20; o++) {
            System.out.println(o++);
        }
        System.out.println("Even Numbers");
        for (int e=2; e<22; e++) {
            System.out.println(e++);
        }
    }
}
